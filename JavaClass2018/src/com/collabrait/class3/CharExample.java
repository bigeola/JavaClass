package com.collabrait.class3;

public class CharExample {
	public static void main(String [] args) {
		String str =  "Welcome to String handling turorial";
		
		char char1 = str.charAt(0);
		System.out.println("character at 0 index is: " + char1);
		
		char char5 = str.charAt(5);
		System.out.println("character at 5th index is: " + char5);
		
		char char11 = str.charAt(11);
		System.out.println("character at 11th index is: " + char11);
		
		char char20 = str.charAt(20);
		System.out.println("character at 20th index is: " + char20);
	}

}
