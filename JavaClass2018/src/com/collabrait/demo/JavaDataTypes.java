package com.collabrait.demo;

public class JavaDataTypes {
	public static void main(String[] args) {
		String hw = "Java Class";
		
		boolean nameof = false;
		byte valueofbyte = -128;
		char character = '1';
		short num = 30;
		int integer = 50000;
		long long_number = -2L;
		float floater = 3.5557F;
		double pi = 3.14;
		
		
		System.out.println("Hello World!");
		System.out.println(hw);
		System.out.println(nameof);
		System.out.println(character);
		System.out.println(num);
		System.out.println(valueofbyte);
		System.out.println(integer);
		System.out.println(long_number);
		System.out.println(floater);
		System.out.println(pi);
		
	}

}
