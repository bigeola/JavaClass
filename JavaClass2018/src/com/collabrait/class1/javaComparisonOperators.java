package com.collabrait.class1;

public class javaComparisonOperators {
	public static void main(String [] args) {
		int num1 = 10;
		int num2 = 50;
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		System.out.println(num1 > num2);
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2);
	}

}
